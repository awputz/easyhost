import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    hover?: boolean
    elevated?: boolean
  }
>(({ className, hover = false, elevated = false, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-xl border border-navy-100 bg-white text-navy-900 transition-all duration-200",
      hover && "hover:border-navy-200 hover:shadow-md hover:-translate-y-0.5",
      elevated && "shadow-sm",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

